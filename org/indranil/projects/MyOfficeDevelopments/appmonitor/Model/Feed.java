package org.indranil.projects.MyOfficeDevelopments.appmonitor.Model;

public class Feed {
	
	int id;
	String feed_name;
	String feed_path;
	String feed_sla;
	String server_name;
	String username;
	String password;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getFeed_name() {
		return feed_name;
	}
	public void setFeed_name(String feed_name) {
		this.feed_name = feed_name;
	}
	public String getFeed_path() {
		return feed_path;
	}
	public void setFeed_path(String feed_path) {
		this.feed_path = feed_path;
	}
	public String getFeed_sla() {
		return feed_sla;
	}
	public void setFeed_sla(String feed_sla) {
		this.feed_sla = feed_sla;
	}
	public String getServer_name() {
		return server_name;
	}
	public void setServer_name(String server_name) {
		this.server_name = server_name;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}

}
